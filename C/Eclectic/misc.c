#include <stdint.h>

/****************************************************************************************************
 * Function Definition (Public)
 ****************************************************************************************************/

/*** Reflect 8 ***/
uint8_t misc_reflect8(const uint8_t Data)
{
    uint8_t reflectedData = 0x00;

    /*** Reflect 8 ***/
    for(size_t bit = 0; bit < 8; bit++)
        reflectedData |= ((Data & (1 << bit)) >> bit) << (7 - bit);

    return reflectedData;
}
